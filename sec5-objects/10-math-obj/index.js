// some built-in methods and properties of the built-in Math object

Math.random(); // returns a random number between 0 and 1

Math.round(1.7); // returns 2. rounds to the nearest integer

Math.max(1, 2, 3); // returns 3. returns the largest of the given numbers

Math.min(1, 2, 3); // returns 1. returns the smallest of the given numbers